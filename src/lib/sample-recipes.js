// Function to get a recipe based on user query
export function getRecipeByQuery(query) {
  const queryLower = query.toLowerCase();

  // Check for specific recipe types first
  if (queryLower.includes('pretzel')) {
    return sampleRecipes[1]; // Pretzel recipe
  }

  // Check for pasta variations
  if (queryLower.includes('pasta')) {
    if (queryLower.includes('quick')) {
      return sampleRecipes[0]; // Quick Garlic Parmesan Pasta
    } else if (queryLower.includes('creamy')) {
      return sampleRecipes[2]; // Creamy Pasta recipe
    } else if (queryLower.includes('healthy') || queryLower.includes('vegetable')) {
      return sampleRecipes[5]; // Modified pasta with vegetables
    }
    // Default pasta recipe if no specific type mentioned
    return sampleRecipes[0]; // Quick Garlic Parmesan Pasta
  }

  // Check for main dishes
  if (
    queryLower.includes('chicken') ||
    queryLower.includes('bell pepper') ||
    queryLower.includes('rice') ||
    queryLower.includes('stir fry') ||
    queryLower.includes('stir-fry')
  ) {
    return sampleRecipes[3]; // Chicken stir-fry
  }

  // Check for dietary preferences
  if (queryLower.includes('vegetarian') || queryLower.includes('vegan')) {
    return sampleRecipes[4]; // Vegetarian curry
  }

  if (
    queryLower.includes('gluten-free') ||
    queryLower.includes('dessert') ||
    queryLower.includes('brownie')
  ) {
    return sampleRecipes[5]; // Gluten-free brownies
  }

  // Check for general categories
  if (queryLower.includes('healthy') || queryLower.includes('light')) {
    return sampleRecipes[5]; // Modified pasta with vegetables
  }

  // Return a random recipe if no specific match is found
  const randomIndex = Math.floor(Math.random() * sampleRecipes.length);
  return sampleRecipes[randomIndex];
}
